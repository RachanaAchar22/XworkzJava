package com.xworkz.Encapsulation;

public class Youtube {

	private String theme;
	private int noOfLikes;
	private int comments;
	private String name;
	private String youtubeId;
	private int friends;
	private int subcription;
	private String language;
	private int share;
	private boolean privacy;
	private int noOfAccount;
	private String founder;
	private boolean membership;
	private boolean history;

	public String getTheme() {
		return this.theme;
	}

	public void setTheme(String theme) {
		this.theme = theme;
	}

	public int getNoOfLikes() {
		return this.noOfLikes;
	}

	public void setNoOfLikes(int noOfLikes) {
		this.noOfLikes = noOfLikes;
	}

	public int getComments() {
		return this.comments;
	}

	public void setComments(int comments) {
		this.comments = comments;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getYoutubeId() {
		return this.youtubeId;
	}

	public void setYoutubeId(String youtubeId) {
		this.youtubeId = youtubeId;
	}

	public int getFriends() {
		return this.friends;
	}

	public void setFriends(int friends) {
		this.friends = friends;
	}

	public int getSubcription() {
		return this.subcription;
	}

	public void setSubcription(int subcription) {
		this.subcription = subcription;
	}

	public String getLanguage() {
		return this.language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

	public int getShare() {
		return this.share;
	}

	public void setShare(int share) {
		this.share = share;
	}

	public boolean isPrivacy() {
		return this.privacy;
	}

	public void setPrivacy(boolean privacy) {
		this.privacy = privacy;
	}

	public int getNoOfAccount() {
		return this.noOfAccount;
	}

	public void setNoOfAccount(int noOfAccount) {
		this.noOfAccount = noOfAccount;
	}

	public String getFounder() {
		return this.founder;
	}

	public void setFounder(String founder) {
		this.founder = founder;
	}

	public boolean isMembership() {
		return this.membership;
	}

	public void setMembership(boolean membership) {
		this.membership = membership;
	}

	public boolean isHistory() {
		return this.history;
	}

	public void setHistory(boolean history) {
		this.history = history;
	}
}
