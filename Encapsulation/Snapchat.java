package com.xworkz.Encapsulation;

public class Snapchat {

	private String theme;
	private int closeFriends;
	private int noOfSnaps;
	private int groups;
	private String chat;
	private int Friends;
	private int storyViews;
	private int noOfFilters;
	private String font;
	private boolean music;
	private boolean location;
	private int filters;
	private String privacy;
	private String snapId;

	public String getTheme() {
		return this.theme;
	}

	public void setTheme(String theme) {
		this.theme = theme;
	}

	public int getCloseFriends() {
		return this.closeFriends;
	}

	public void setCloseFriends(int closeFriends) {
		this.closeFriends = closeFriends;
	}

	public int getNoOfSnaps() {
		return this.noOfSnaps;
	}

	public void setNoOfSnaps(int noOfSnaps) {
		this.noOfSnaps = noOfSnaps;
	}

	public int getGroups() {
		return this.groups;
	}

	public void setGroups(int groups) {
		this.groups = groups;
	}

	public String getChat() {
		return this.chat;
	}

	public void setChat(String chat) {
		this.chat = chat;
	}

	public int getFriends() {
		return this.Friends;
	}

	public void setFriends(int friends) {
		Friends = friends;
	}

	public int getStoryViews() {
		return this.storyViews;
	}

	public void setStoryViews(int storyViews) {
		this.storyViews = storyViews;
	}

	public int getNoOfFilters() {
		return this.noOfFilters;
	}

	public void setNoOfFilters(int noOfFilters) {
		this.noOfFilters = noOfFilters;
	}

	public String getFont() {
		return this.font;
	}

	public void setFont(String font) {
		this.font = font;
	}

	public boolean isMusic() {
		return this.music;
	}

	public void setMusic(boolean music) {
		this.music = music;
	}

	public boolean isLocation() {
		return this.location;
	}

	public void setLocation(boolean location) {
		this.location = location;
	}

	public int getFilters() {
		return this.filters;
	}

	public void setFilters(int filters) {
		this.filters = filters;
	}

	public String getPrivacy() {
		return this.privacy;
	}

	public void setPrivacy(String privacy) {
		this.privacy = privacy;
	}

	public String getSnapId() {
		return this.snapId;
	}

	public void setSnapId(String snapId) {
		this.snapId = snapId;
	}

}
