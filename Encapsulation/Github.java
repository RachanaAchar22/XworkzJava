package com.xworkz.Encapsulation;

public class Github {

	private String repositoryName;
	private boolean profilePic;
	private String userName;
	private int noOfRepo;
	private String access;
	private boolean notification;
	private String language;
	private String theme;
	private String projectName;
	private int noOfProjects;
	private String link;
	private int noOfBranches;
	private int noOfPackages;
	private boolean issue;

	public String getRepositoryName() {
		return this.repositoryName;
	}

	public void setRepositoryName(String repositoryName) {
		this.repositoryName = repositoryName;
	}

	public boolean isProfilePic() {
		return this.profilePic;
	}

	public void setProfilePic(boolean profilePic) {
		this.profilePic = profilePic;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public int getNoOfRepo() {
		return noOfRepo;
	}

	public void setNoOfRepo(int noOfRepo) {
		this.noOfRepo = noOfRepo;
	}

	public String getAccess() {
		return this.access;
	}

	public void setAccess(String access) {
		this.access = access;
	}

	public boolean isNotification() {
		return this.notification;
	}

	public void setNotification(boolean notification) {
		this.notification = notification;
	}

	public String getLanguage() {
		return this.language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

	public String getTheme() {
		return this.theme;
	}

	public void setTheme(String theme) {
		this.theme = theme;
	}

	public String getProjectName() {
		return this.projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public int getNoOfProjects() {
		return this.noOfProjects;
	}

	public void setNoOfProjects(int noOfProjects) {
		this.noOfProjects = noOfProjects;
	}

	public String getLink() {
		return this.link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public int getNoOfBranches() {
		return this.noOfBranches;
	}

	public void setNoOfBranches(int noOfBranches) {
		this.noOfBranches = noOfBranches;
	}

	public int getNoOfPackages() {
		return this.noOfPackages;
	}

	public void setNoOfPackages(int noOfPackages) {
		this.noOfPackages = noOfPackages;
	}

	public boolean isIssue() {
		return this.issue;
	}

	public void setIssue(boolean issue) {
		this.issue = issue;
	}

}
